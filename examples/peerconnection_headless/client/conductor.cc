class DummySetSessionDescriptionObserver
    : public webrtc::SetSessionDescriptionObserver {
 public:
  static DummySetSessionDescriptionObserver* Create() {
    return new rtc::RefCountedObject<DummySetSessionDescriptionObserver>();
  }
  virtual void OnSuccess() { RTC_LOG(INFO) << __FUNCTION__; }
  virtual void OnFailure(webrtc::RTCError error) {
    RTC_LOG(INFO) << __FUNCTION__ << " " << ToString(error.type()) << ": "
                  << error.message();
  }
};

class CustomVideoPreprocessor : public webrtc::test::TestVideoCapturer::FramePreprocessor {

    webrtc::VideoFrame Preprocess(const webrtc::VideoFrame& frame) {
        auto buffer = frame.video_frame_buffer();
        auto i420buf = buffer->GetI420();
        const int height_padding = 4;
        uint32_t index = base::debug::DebugUtils::getTimestampMs();
        const uint8_t* data_y = i420buf->DataY() + buffer->width() * (buffer->height() - height_padding);
        bool padded = data_y[0] == 0 && data_y[1] == 0xff && data_y[0] == 0 && data_y[1] == 0xff;
        if (padded) {
            data_y = i420buf->DataY() + buffer->width() * (buffer->height() - height_padding + 1);
            index = ((uint32_t)data_y[0] << 24) | ((uint32_t)data_y[1] << 16) | 
                ((uint32_t)data_y[2] << 8) | ((uint32_t)data_y[3]);
        } else {
            index = frame.frame_sequence();
        }
        RTC_LOG(INFO) << "Video preprocessor frame size: " << buffer->width() << "x" << 
            buffer->height() << ", chroma size: " << i420buf->ChromaWidth() 
            << "x" << i420buf->ChromaHeight() << ", " << ", index: " << index;
        
        rtc::scoped_refptr<webrtc::I420Buffer> buffer2 = webrtc::I420Buffer::Copy(i420buf->width(), 
                i420buf->height() - height_padding, i420buf->DataY(), i420buf->StrideY(), 
                i420buf->DataU(), i420buf->StrideU(), i420buf->DataV(), i420buf->StrideV()); 
        webrtc::VideoFrame frame2 = webrtc::VideoFrame::Builder()
            .set_video_frame_buffer(buffer2)
            .set_timestamp_rtp(frame.timestamp())
            .set_frame_sequence(index)
            .set_timestamp_us(frame.timestamp_us())
            .set_rotation(frame.rotation())
            .build();
        return frame2;
    }
};

class CapturerTrackSource : public webrtc::VideoTrackSource {
 public:
  static rtc::scoped_refptr<CapturerTrackSource> Create(std::string resolution) {
    const size_t kWidth = 640;
    const size_t kHeight = 480;
    const size_t kFps = 30;
    std::unique_ptr<webrtc::test::VcmCapturer> capturer;
    std::unique_ptr<webrtc::VideoCaptureModule::DeviceInfo> info(
        webrtc::VideoCaptureFactory::CreateDeviceInfo());
    if (!info) {
      return nullptr;
    }
    int num_devices = info->NumberOfDevices();
    for (int i = num_devices - 1; i >= 0; --i) {
      capturer = absl::WrapUnique(
          webrtc::test::VcmCapturer::Create(kWidth, kHeight, kFps, i));
      if (capturer) {
        std::unique_ptr<webrtc::test::TestVideoCapturer::FramePreprocessor> processor(new CustomVideoPreprocessor());
        capturer->SetFramePreprocessor(std::move(processor));
        capturer->SetResolution(resolution);
        return new rtc::RefCountedObject<CapturerTrackSource>(
            std::move(capturer));
      }
    }

    return nullptr;
  }

 protected:
  explicit CapturerTrackSource(
      std::unique_ptr<webrtc::test::VcmCapturer> capturer)
      : VideoTrackSource(/*remote=*/false), capturer_(std::move(capturer)) {}

 private:
  rtc::VideoSourceInterface<webrtc::VideoFrame>* source() override {
    return capturer_.get();
  }
  std::unique_ptr<webrtc::test::VcmCapturer> capturer_;
};

